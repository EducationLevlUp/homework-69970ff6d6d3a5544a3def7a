# ---------------------------------------------------------------------------
# Утилиты
# ---------------------------------------------------------------------------

def set_run_font(run, size: int = 11, bold: bool = False, italic: bool = False, color: str | None = None) -> None:
    """Установить шрифт для run."""
    run.font.size = Pt(size)
    run.font.bold = bold
    run.font.italic = italic
    if color:
        run.font.color.rgb = RGBColor.from_string(color)


def add_heading_styled(doc: Document, text: str, level: int = 1) -> None:
    """Добавить заголовок с форматированием."""
    heading = doc.add_heading(text, level=level)
    for run in heading.runs:
        run.font.color.rgb = RGBColor.from_string("1D1D1F")
    return heading


def add_table(doc: Document, headers: list[str], rows: list[list[str]]) -> None:
    """Добавить таблицу с заголовками и строками."""
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.style = "Light Grid Accent 1"
    table.alignment = WD_TABLE_ALIGNMENT.CENTER

    # Заголовки
    for i, header in enumerate(headers):
        cell = table.rows[0].cells[i]
        cell.text = header
        for paragraph in cell.paragraphs:
            for run in paragraph.runs:
                set_run_font(run, size=10, bold=True)

    # Строки
    for row_idx, row_data in enumerate(rows, start=1):
        for col_idx, cell_text in enumerate(row_data):
            cell = table.rows[row_idx].cells[col_idx]
            cell.text = cell_text
            for paragraph in cell.paragraphs:
                for run in paragraph.runs:
                    set_run_font(run, size=10)


def add_bullet_list(doc: Document, items: list[str]) -> None:
    """Добавить маркированный список."""
    for item in items:
        p = doc.add_paragraph(item, style="List Bullet")
        for run in p.runs:
            set_run_font(run, size=11)


def add_numbered_list(doc: Document, items: list[str]) -> None:
    """Добавить нумерованный список."""
    for item in items:
        p = doc.add_paragraph(item, style="List Number")
        for run in p.runs:
            set_run_font(run, size=11)


def add_block_quote(doc: Document, text: str) -> None:
    """Добавить цитату (курсив с отступом)."""
    p = doc.add_paragraph()
    run = p.add_run(f"«{text}»")
    set_run_font(run, size=11, italic=True, color="555555")
    p.paragraph_format.left_indent = Inches(0.5)
    p.paragraph_format.right_indent = Inches(0.5)


def add_separator(doc: Document) -> None:
    """Добавить разделитель."""
    p = doc.add_paragraph()
    run = p.add_run("─" * 60)
    set_run_font(run, size=8, color="CCCCCC")
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER


